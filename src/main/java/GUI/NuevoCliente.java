     public void limpiar(){
         txtCelular.setText("");
          txtColor.setText("");
          txtNombreCliente.setText("");
          txtNombreDueno.setText("");
          txtObservacion.setText("");
          txtRaza.setText("");
          boxAlergico.setSelectedIndex(0);
          boxAtencion.setSelectedIndex(0);
     }
     private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
          
          //Obtener datos mascota
          
          String color = txtColor.getText();
          String nbrMascota =txtNombreCliente.getText();
          String observacion =txtObservacion.getText();
          String raza =txtRaza.getText();
          
          String alergico =(String) boxAlergico.getSelectedItem();
          String atencion =(String) boxAtencion.getSelectedItem();
          
          //datos dueno
          String celular = txtCelular.getText();
          String nbrDueno = txtNombreCliente.getText();
          
          controlLogica.guardar(color,nbrMascota,raza,observacion, alergico, atencion, celular,nbrDueno);
          
          JOptionPane optionPane = new JOptionPane("Guardado correctamente!");
          optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
          JDialog dialog = optionPane.createDialog("Guardado Exitoso");
          dialog.setAlwaysOnTop(true);
          dialog.setVisible(true);
          
          limpiar();
     }//GEN-LAST:event_btnGuardarActionPerformed

