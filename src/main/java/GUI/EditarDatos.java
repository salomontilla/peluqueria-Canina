    //Rellena los espacion con los valores antiguos para que sean modificados (invocado en la linea 28)
    private void cargarDatos(int numCliente) {
        
        this.masco = controlLogica.traerMascota(numCliente);
        txtCelular.setText(masco.getDueno().getCelular());
        txtColor.setText(masco.getColor());
        txtNombreCliente.setText(masco.getNbrMascota());
        txtNombreDueno.setText(masco.getDueno().getNbrDueno());
        txtObservacion.setText(masco.getObservaciones());
        txtRaza.setText(masco.getRaza());
        
        if(masco.getAlergia().equals("Si")){
            boxAlergico.setSelectedIndex(1);
        }
        else{
            boxAtencion.setSelectedIndex(0);
        }
        
        if(masco.getEspecial().equals("Si")){
            boxAlergico.setSelectedIndex(1);
        }
        else{
            boxAtencion.setSelectedIndex(0);
        }
    }
