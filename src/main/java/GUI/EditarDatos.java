     private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
          
        //Obtener datos mascota
        String color = txtColor.getText();
        String nbrMascota =txtNombreCliente.getText();
        String observacion =txtObservacion.getText();
        String raza =txtRaza.getText();
        String alergico =(String) boxAlergico.getSelectedItem();
        String atencion =(String) boxAtencion.getSelectedItem();

        //datos dueno
        String celular = txtCelular.getText();
        String nbrDueno = txtNombreCliente.getText();
        
        //llama el metodo editar mascota de la controladora logica (el funcionamiento del metodo se explica en la controlLogica) 
        controlLogica.editarMascota(masco,color,nbrMascota,raza,observacion, alergico, atencion, celular,nbrDueno);
        
        //Crea el mensaje
        JOptionPane optionPane = new JOptionPane("Cambios guardados correctamente!");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Guardado Exitoso");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        
        //Actualiza la pantalla de visualizacion de datos
        VerClientes panta = new VerClientes();
        panta.setVisible(true);
        panta.setLocationRelativeTo(null);
        this.dispose();
     }//GEN-LAST:event_btnEditarActionPerformed

